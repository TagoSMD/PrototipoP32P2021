    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        EmpleadosDAO tipodAO = new EmpleadosDAO();
        Empleado tipoEliminar = new Empleado();
        //Metodo Eliminar utilizando el codigo de tipo transaccion para eliminarlo de la bd
        tipoEliminar.setCodigo_Empleado((txtbuscado.getText()));
        tipodAO.delete(tipoEliminar);
       try {
           llenadoDeTablas();
       } catch (SQLException ex) {
           Logger.getLogger(FrmEmpleado.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        EmpleadosDAO tipodao = new EmpleadosDAO();
        Empleado insertartipo = new Empleado();
        insertartipo.setCodigo_Empleado(TxtCodigo.getText());
        insertartipo.setCodigo_Tienda(Cbox_Tipo.getSelectedItem().toString());
        insertartipo.setNombre_Empleado(TxtNombre.getText());
        insertartipo.setEstado_Empleado(TxtEstado.getText());
   

        tipodao.insert(insertartipo);
       try {
           llenadoDeTablas();
       } catch (SQLException ex) {
           Logger.getLogger(FrmEmpleado.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_btnRegistrarActionPerformed

