 // metodo de llenado de tablas automaticamente aparecen los datos guardados en bd y se despliega en automatico ademas crea las tablas en el jtable
 public void llenadoDeTablas() {
      try {
          DefaultTableModel modelo = new DefaultTableModel();
          modelo.addColumn("Codigo devolucion");
          modelo.addColumn("Codigo Renta");
          modelo.addColumn("Fecha vencimiento");

          
          DevolucionDAO TipoTDAO = new DevolucionDAO();
          List<Devolucion> tipot = TipoTDAO.select();
          JTableTransaccion.setModel(modelo);
          String[] dato = new String[3];
          for (int i = 0; i < tipot.size(); i++) {
              dato[0] = tipot.get(i).getCodigo_Devolucion();
              dato[1] = tipot.get(i).getCodigo_Renta();
              dato[2] = tipot.get(i).getFecha_Devolucion();
              modelo.addRow(dato);
          }
      } catch (SQLException ex) {
          Logger.getLogger(FrmTiendas.class.getName()).log(Level.SEVERE, null, ex);
      }
    }
