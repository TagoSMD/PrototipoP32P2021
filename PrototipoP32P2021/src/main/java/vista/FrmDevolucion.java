    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        DevolucionDAO tipodao = new DevolucionDAO();
        Devolucion insertartipo = new Devolucion();
        insertartipo.setCodigo_Devolucion(TxtCodigo.getText());
         insertartipo.setCodigo_Renta(Cbox_Renta.getSelectedItem().toString());
        insertartipo.setFecha_Devolucion(TxtVencimiento.getText());
     

        tipodao.insert(insertartipo);
        llenadoDeTablas();
    }//GEN-LAST:event_btnRegistrarActionPerformed

